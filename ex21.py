def add(a, b):
    print(f"ADDING {a} + {b}")
    return a+b

def subtract(a, b):
    print(f"SUNTRACTING {a} - {b}")
    return a-b

def multiply(a, b):
    print(f"MULTIPLYING {a} * {b}")
    return a*b
